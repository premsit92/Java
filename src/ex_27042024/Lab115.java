package ex_27042024;

public class Lab115 {
    public static void main(String[] args) {
        //while
        //A- Initialization
        //B - Condition
        //C - Incr/Decr/Updation

        int i =1; //Initialization
        while (i<=10) //Condition
        {
            System.out.println(i);
            i++; //increament/decrement/updation
        }
        for(int j =1; j<=10; j++)
        {
            System.out.println(j);
        }
    }
}
