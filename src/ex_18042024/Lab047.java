package ex_18042024;

public class Lab047 {
    public static void main(String[] args) {
        //Pre increment
        int a = 11;
        System.out.println(++a);
        System.out.println(a);


        //Post Increment
        int b = 12;
        System.out.println(b++);
        System.out.println(b);
    }
}
