package ex_18042024;

public class Lab044 {
    public static void main(String[] args) {
        //Increment (++) / Decrement (--) Operators

        int a = 10;
        int b = ++a; // ++a = a + 1; (Value increase first --> Assign the value.)

        System.out.println(b);
        System.out.println(a);
    }
}
